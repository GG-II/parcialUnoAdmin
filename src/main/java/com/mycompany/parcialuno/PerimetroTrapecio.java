package com.mycompany.parcialuno;

public class PerimetroTrapecio {
    
    /**
     * Método que calcula el perimetro de un trapecio.
     * @param l1 Lado 1
     * @param l2 Lado 2
     * @param l3 Lado 3
     * @param l4 Lado 4
     * @return Resultado del calculo
     */
    public int perimetroTrap(int l1, int l2, int l3, int l4) {
        return l1 + l2 + l3 + l4;
    }
}
